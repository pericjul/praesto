function deepMerge(modules) {
	const out = { de: {}, en: {}, fr: {}, it: {} };
	for (const mod of modules) {
		if (!mod) continue;
		for (const lang of LANGS) {
			Object.assign(out[lang], mod[lang] ?? {});
		}
	}
	return out;
}

